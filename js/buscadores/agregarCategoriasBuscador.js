function asignarClicksCategorias(button, categoria, bandera) {
  button.addEventListener("click", () => {
    let lista_empresa = document.getElementById("lista_empresa");

    //Presionamos todos los botones antes del nuevo.
    let buttonCategoria = document.getElementsByClassName("buttonCategoria");
    let buttonCategoria_arr = [...buttonCategoria];
    buttonCategoria_arr.forEach((e) => {
      if (e.getAttribute("state") == "true") {
        e.click();
      }
    });

    if (bandera == false) {
      //Antes de hacer cambios los guardamos
      localStorage.setItem(
        "contenidoDeEmpresasBuscadorMemoria_anterior",
        lista_empresa.innerHTML
      );

      //Accdemos a los elementos de la categoria seleccionada
      let arrayEmpresaCategoria = empresasPorCategorias[categoria];
      lista_empresa.innerHTML = "";

      //Metemos los elementos en la lista de empresas
      let textoEmpresa = "";
      arrayEmpresaCategoria.forEach((empresa) => {
        textoEmpresa += `<li>${findCompanyWithCategory(empresa.name,arrayEmpresaCategoria)}</li>`;
      });
      lista_empresa.innerHTML = textoEmpresa;

      button.setAttribute("state", "true");
      bandera = true;
    } else {
      let empresaOriginal = localStorage.getItem("originalLista");
      empresaOriginal = empresaOriginal.replaceAll(/,/g, '');
      lista_empresa.innerHTML = empresaOriginal;
      button.setAttribute("state", "false");
      bandera = false;
    }
  });
}

